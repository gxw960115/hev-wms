SELECT
  ID
, FILE_NAME
, SAVE_FILE_NAME
, FILE_PATH_1
, FILE_PATH_2
, STATUS
, TYPE
, REMARKS
, LAST_UPD_BY
, DATE_FORMAT(
		LAST_UPD_DT,
		'%Y-%m-%d %H:%i:%s'
	) LAST_UPD_DT
, CREATED_BY
, DATE_FORMAT(
		CREATED_DT,
		'%Y-%m-%d %H:%i:%s'
	) CREATED_DT
, VERSIONS
, COUNTRY_CODE
FROM upload_file
where COUNTRY_CODE='6600'
