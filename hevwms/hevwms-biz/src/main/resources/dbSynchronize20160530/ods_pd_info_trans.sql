SELECT
	ROW_ID,
	ORDER_NO,
	ORDER_TYPE,
	ORDER_STATUS,
	PLANT,
	WAREHOUSE_CODE,
	WAREHOUSE_NAME,
	CUSTOMER_MODEL,
	LOCATION,
	MATERIAL_NO,
	MATERIAL_DESP,
	UNIT,
	ACTUAL_QTY,
	BEGIN_DATE,
	END_DATE,
	CREATE_BY,
	DATE_FORMAT(
		CREATE_DATE,
		'%Y-%m-%d %H:%i:%s'
	) CREATE_DATE,
	MODIFY_BY,
	DATE_FORMAT(
		MODIFY_DATE,
		'%Y-%m-%d %H:%i:%s'
	) MODIFY_DATE,
	FLAG,
	VERSION
FROM
	ods_pd_info
WHERE
	MODIFY_DATE > DATE_ADD(NOW(), INTERVAL - 30 DAY)
AND PLANT IN (
	SELECT DISTINCT
		cd.`CODE`
	FROM
		cd_factory cd
	WHERE
		cd.country_code = '6600'
)