SELECT
	ROW_ID,
	ORDER_NO,
	ORDER_ITEM,
	ORDER_TYPE,
	DOC_TPYE,
	SAP_ORDER_NO,
	SAP_ORDER_ITEM,
	BATCH_NO,
	PLANT,
	WAREHOUSE_CODE,
	WAREHOUSE_NAME,
	LOCATION,
	MATERIAL_NO,
	CUSTOMER_MODEL,
	MATERIAL_DESP,
	SCANNED_QTY,
	UNIT,
	VENDOR_CODE,
	VENDOR_NAME,
	DELIVERY_CODE,
	DELIVERY_NAME,
	DATE_FORMAT(
		DELIVERY_DATE,
		'%Y-%m-%d %H:%i:%s'
	) DELIVERY_DATE,
	CAR_NO,
	HAND_FLAG,
	CREATE_BY,
	DATE_FORMAT(
		CREATE_DATE,
		'%Y-%m-%d %H:%i:%s'
	) CREATE_DATE,
	MODIFY_BY,
	DATE_FORMAT(
		MODIFY_DATE,
		'%Y-%m-%d %H:%i:%s'
	) MODIFY_DATE,
	FLAG,
	VERSION,
	SAP_FLAG,
	SAP_MSG,
	SAP_DOC_NO,
	SAP_SEND_ID,
	POSTING_DATE,
	IN_OUT_FLAG,
	IN_OUT_MSG,
	REQUIRE_QTY,
	ORDER_CLOSE,
	CAR_ID,
	PRESACN_FLAG,
	CHECK_FLAG,
	CAR_FLAG,
	HAND_POST_FLAG,
	VECHILE_TYPE
FROM
	ods_order_info
WHERE
	MODIFY_DATE > DATE_ADD(NOW(), INTERVAL - 30 DAY)
AND PLANT IN (
	SELECT DISTINCT
		cd.`CODE`
	FROM
		cd_factory cd
	WHERE
		cd.country_code = '6600'
)