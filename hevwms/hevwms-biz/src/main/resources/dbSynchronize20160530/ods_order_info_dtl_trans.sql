SELECT
	ROW_ID,
	ORDER_TYPE,
	DOC_TYPE,
	SAP_ORDER_NO,
	SAP_ORDER_ITEM,
	BATCH_NO,
	PLANT,
	CUSTOMER_MODEL,
	LOCATION,
	MATERIAL_NO,
	MATERIAL_DESP,
	UNIT,
	BARCODE,
	VENDOR_CODE,
	VENDOR_NAME,
	DELIVERY_CODE,
	DELIVERY_NAME,
	SCANNED_BY,
	DATE_FORMAT(
		SCANNED_DATE,
		'%Y-%m-%d %H:%i:%s'
	) SCANNED_DATE,
	DATE_FORMAT(
		DELIVERY_DATE,
		'%Y-%m-%d %H:%i:%s'
	) DELIVERY_DATE,
	ORDER_ID,
	ORDER_NO,
	CAR_NO,
	CREATE_BY,
	DATE_FORMAT(
		CREATE_DATE,
		'%Y-%m-%d %H:%i:%s'
	) CREATE_DATE,
	MODIFY_BY,
	DATE_FORMAT(
		MODIFY_DATE,
		'%Y-%m-%d %H:%i:%s'
	) MODIFY_DATE,
	FLAG,
	VERSION,
	QTY,
	ORDER_ITEM,
	SERIAL_NO,
	FINISH_FLAG,
	IN_OUT_FLAG,
	IN_OUT_MSG,
	USED_FLAG,
	SUB_LOCATION
FROM
	ods_order_info_dtl
WHERE
	MODIFY_DATE > DATE_ADD(NOW(), INTERVAL - 30 DAY)
AND PLANT IN (
	SELECT DISTINCT
		cd.`CODE`
	FROM
		cd_factory cd
	WHERE
		cd.country_code = '6600'
)